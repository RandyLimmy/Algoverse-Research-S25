import os
from Bio import Entrez, Medline
import pandas as pd
from dotenv import load_dotenv
load_dotenv()

Entrez.email = os.getenv("ENTREZ_EMAIL", "your-email@example.com")

# HPO terms for the case
hpo_terms = [
    "hypotonia",
    "intellectual disability",
    "delayed gross motor development",
    "autistic behavior",
    "abnormal facial shape",
    "sleep abnormality"
]
ontology_categories = ["Cognitive", "Craniofacial / Dysmorphia", "Motor"]

def build_diagnosis_free_query(hpo_terms, ontology_categories):
    hpo_part = " OR ".join(hpo_terms)
    category_part = " OR ".join(ontology_categories)
    return f"({hpo_part}) OR ({category_part})"

def search_pubmed(query, max_results=30):
    handle = Entrez.esearch(db="pubmed", term=query, retmax=max_results)
    record = Entrez.read(handle)
    handle.close()
    return record["IdList"]

def fetch_articles(pmids):
    if not pmids:
        return []
    handle = Entrez.efetch(db="pubmed", id=",".join(pmids), rettype="medline", retmode="text")
    records = list(Medline.parse(handle))
    handle.close()
    return records

def articles_to_df(records):
    data = []
    for rec in records:
        data.append({
            "PMID": rec.get("PMID", ""),
            "Title": rec.get("TI", ""),
            "Abstract": rec.get("AB", ""),
            "Authors": ", ".join(rec.get("AU", [])),
            "Journal": rec.get("JT", ""),
            "Year": rec.get("DP", "")
        })
    return pd.DataFrame(data)

query = build_diagnosis_free_query(hpo_terms, ontology_categories)
pmids = search_pubmed(query)
records = fetch_articles(pmids)
df = articles_to_df(records)
df.to_csv("pubmed_results.csv", index=False)
print(f"✅ Retrieved {len(df)} abstracts.")

