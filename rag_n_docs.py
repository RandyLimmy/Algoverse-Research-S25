import os
from langchain.text_splitter import RecursiveCharacterTextSplitter
from langchain_community.vectorstores import Chroma
from langchain_openai import OpenAIEmbeddings, ChatOpenAI
from langchain.prompts import PromptTemplate

from dotenv import load_dotenv
load_dotenv()
# Set OPENAI_API_KEY in your .env file

# Split abstracts into chunks
text_splitter = RecursiveCharacterTextSplitter(chunk_size=1000, chunk_overlap=150)
all_chunks = []

for idx, row in df.iterrows():
    abstract = row["Abstract"]
    if not isinstance(abstract, str) or len(abstract.strip()) < 10:
        continue

    chunks = text_splitter.create_documents(
        texts=[abstract],
        metadatas=[{
            "pmid": row["PMID"],
            "title": row["Title"],
            "authors": row["Authors"],
            "journal": row["Journal"],
            "year": row["Year"]
        }]
    )
    all_chunks.extend(chunks)

print(f"✅ Total Chunks: {len(all_chunks)}")

embedding_model = OpenAIEmbeddings(model="text-embedding-3-small")
vectorstore = Chroma.from_documents(all_chunks, embedding_model, persist_directory="rag_pubmed_db")
vectorstore.persist()
retriever = vectorstore.as_retriever()
llm = ChatOpenAI(model="gpt-3.5-turbo", temperature=0.3)

# ------------------ STEP 3: Run Diagnosis w/ Unique Abstract Control ------------------

# Helper to get N unique PMIDs
from collections import defaultdict

def get_unique_pmids(docs, num_unique):
    seen = set()
    unique_docs = []
    for doc in docs:
        pmid = doc.metadata.get("pmid")
        if pmid not in seen:
            seen.add(pmid)
            unique_docs.append(doc)
        if len(unique_docs) == num_unique:
            break
    return unique_docs

